# Assuming an arbitrary age
age = 20
square_of_age = age ** 2
print("Square of age:", square_of_age)

# Assuming an arbitrary height
height = 5.2 # in meters
weight = 54 # in kilograms
bmi = weight / (height ** 2)
print("BMI:", bmi)
